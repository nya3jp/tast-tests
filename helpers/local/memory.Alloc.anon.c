#include <stdio.h>
#include <stdlib.h>
#include <sys/mman.h>
#include <sys/types.h>

typedef struct AllocStackNode_
{
  struct AllocStackNode_ *prev;
  unsigned char random[1048576 - sizeof(struct AllocStackNode_ *)];
} AllocStackNode;

static unsigned const char random_page[];

static AllocStackNode *alloc_stack = NULL;
static size_t alloc_size = 0;
static size_t alloc_count = 0;

void allocNode(size_t page_rand_end)
{
  AllocStackNode *node =
      mmap(NULL, sizeof(AllocStackNode), PROT_READ | PROT_WRITE,
           MAP_PRIVATE | MAP_ANONYMOUS, -1, 0);
  if (node == MAP_FAILED)
  {
    perror("mmap");
    exit(EXIT_FAILURE);
  }
  node->prev = alloc_stack;
  alloc_stack = node;
  for (size_t i = 0; i < sizeof(node->random); i++)
  {
    size_t page_offset = i & 0xfff;
    if (page_offset < page_rand_end)
    {
      node->random[i] = random_page[page_offset];
    }
    else
    {
      // If, within the current page, we are past the page_rand_end index,
      // then fill with zeros so we have some compressability.
      node->random[i] = 0;
    }
  }
  alloc_size += sizeof(AllocStackNode);
  alloc_count++;
}

void freeNode()
{
  if (NULL == alloc_stack)
  {
    fprintf(stderr, "nothing to free\n");
    exit(EXIT_FAILURE);
  }
  AllocStackNode *prev = alloc_stack->prev;
  if (0 != munmap(alloc_stack, sizeof(AllocStackNode)))
  {
    perror("munmap");
    exit(EXIT_FAILURE);
  }
  alloc_stack = prev;
  alloc_size -= sizeof(AllocStackNode);
}

void printUsage()
{
  fprintf(stderr, "usage: memory.Alloc.anon <ratio> <oom_score_adj>\n");
  fprintf(stderr, "  ratio - how much to fill each page with random data.\n");
  fprintf(stderr, "    Should be a float in range [0, 1].\n");
  fprintf(stderr, "  oom_score_adj - value to set /proc/self/oom_score_adj\n");
  fprintf(stderr, "    before allocating.\n");
}

int main(int argc, char *argv[])
{
  // Parse arguments.
  if (argc != 3)
  {
    printUsage();
    return 1;
  }

  size_t page_rand_end = 4096;
  float ratio;
  if (1 == sscanf(argv[1], "%f", &ratio))
  {
    page_rand_end = (size_t)(4096.0f * ratio);
  }
  else
  {
    printUsage();
    return 1;
  }

  // Set oom_score_adj.
  FILE *oom_score_adj = fopen("/proc/self/oom_score_adj", "w");
  if (NULL == oom_score_adj)
  {
    perror("failed to open /proc/self/oom_score_adj");
    return 1;
  }
  if (0 > fprintf(oom_score_adj, "%s", argv[2]))
  {
    fprintf(
        stderr,
        "failed to write \"%s\" to /proc/self/oom_score_adj\n",
        argv[2]);
    return 1;
  }
  if (0 > fclose(oom_score_adj))
  {
    fprintf(stderr, "failed to close /proc/self/oom_score_adj\n");
  }

  // Read, allocate, print loop.
  char *buffer = NULL;
  size_t buffer_len = 0;
  ssize_t line_len;
  while (-1 != (line_len = getline(&buffer, &buffer_len, stdin)))
  {
    ssize_t request_size;
    if (line_len == 1)
    {
      break;
    }
    else if (1 == sscanf(buffer, "%zd", &request_size))
    {
      ssize_t alloc_MB = request_size >> 20;
      if (0 != (request_size & 0xfffff))
      {
        fprintf(stderr, "%zd not an even number of MB, truncating to %zd MB\n",
                request_size, alloc_MB);
      }
      while (alloc_MB > 0)
      {
        allocNode(page_rand_end);
        alloc_MB--;
      }
      while (alloc_MB < 0)
      {
        freeNode();
        alloc_MB++;
      }
    }
    else
    {
      fprintf(stderr, "unrecognized operation: %s", buffer);
      return 1;
    }
    fprintf(stdout, "%zd\n", alloc_size);
    fflush(stdout);
  }
  free(buffer);
  return 0;
}

