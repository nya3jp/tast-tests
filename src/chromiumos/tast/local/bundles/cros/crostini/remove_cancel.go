func RemoveCancel(ctx context.Context, s *testing.State) {
	tconn := s.PreValue().(crostini.PreData).TestAPIConn
	cr := s.PreValue().(crostini.PreData).Chrome

	// Use a shortened context for test operations to reserve time for cleanup.
	cleanupCtx := ctx
	ctx, cancel := ctxutil.Shorten(ctx, crostini.PostTimeout)
	defer cancel()
	defer crostini.RunCrostiniPostTest(cleanupCtx, s.PreValue().(crostini.PreData))

	// Open the Linux settings.
	st, err := settings.OpenLinuxSettings(ctx, tconn, cr)
	if err != nil {
		s.Fatal("Failed to open Linux Settings: ", err)
	}
	defer st.Close(cleanupCtx)

	ui := uiauto.New(tconn)
	// Click button Remove and click cancel on the confirm dialog.
	if err := uiauto.Combine("click button Cancel on remove Linux dialog",
		st.ClickRemove(),
		ui.LeftClick(settings.RemoveConfirmDialog.Cancel),
		ui.WaitUntilExists(settings.PageLinux))(ctx); err != nil {
		s.Fatal("Failed to test cancel remove: ", err)
	}

	// Launch Terminal to verify that Crostini still works.
	terminalApp, err := terminalapp.Launch(ctx, tconn)
	if err != nil {
		s.Fatal("Failed to lauch terminal after cancel remove: ", err)
	}
	terminalApp.Close()(ctx)
}
