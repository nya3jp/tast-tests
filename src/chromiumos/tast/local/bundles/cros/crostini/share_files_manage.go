func ShareFilesManage(ctx context.Context, s *testing.State) {
	tconn := s.FixtValue().(crostini.FixtureData).Tconn
	cont := s.FixtValue().(crostini.FixtureData).Cont
	cr := s.FixtValue().(crostini.FixtureData).Chrome

	// Use a shortened context for unshare all folders.
	cleanupCtx := ctx
	ctx, cancel := ctxutil.Shorten(ctx, 5*time.Second)
	defer cancel()

	// Open Files app.
	filesApp, err := filesapp.Launch(ctx, tconn)
	if err != nil {
		s.Fatal("Failed to open Files app: ", err)
	}

	sharedFolders := sharedfolders.NewSharedFolders(tconn)
	// Clean up shared folders in the end.
	defer func() {
		if err := sharedFolders.UnshareAll(cont, cr)(cleanupCtx); err != nil {
			s.Error("Failed to unshare all folders: ", err)
		}
	}()

	if err := uiauto.Combine("share My files and click Manage with Linux on My files",
		sharedFolders.ShareMyFilesOK(ctx, filesApp),
		filesApp.ClickDirectoryContextMenuItem(sharedfolders.MyFiles, sharedfolders.ManageLinuxSharing))(ctx); err != nil {
		s.Fatal("Failed to share My files: ", err)
	}

	sp, err := settings.FindSettingsPage(ctx, tconn, settings.PageNameMSF)
	if err != nil {
		s.Fatal("Failed to find Manage shared folders: ", err)
	}

	if shared, err := sp.GetSharedFolders(ctx); err != nil {
		s.Fatal("Failed to find the shared folders list: ", err)
	} else if want := []string{sharedfolders.MyFiles}; !reflect.DeepEqual(shared, want) {
		s.Fatalf("Failed to verify shared folders list, got %s, want %s", shared, want)
	}
}
