func ResizeInstallation(ctx context.Context, s *testing.State) {
	cr := s.PreValue().(*chrome.Chrome)
	tconn, err := cr.TestAPIConn(ctx)

	if err != nil {
		s.Fatal("Failed to create Test API connection: ", err)
	}

	iOptions := crostini.GetInstallerOptions(s, vm.DebianBuster, false /*largeContainer*/, cr.NormalizedUser())
	iOptions.MinDiskSize = 16 * settings.SizeGB
	iOptions.IsSoftMinimum = true

	// Cleanup.
	defer func() {
		// Get the container.
		cont, err := vm.DefaultContainer(ctx, iOptions.UserName)
		if err != nil {
			s.Fatal("Failed to connect to the container: ", err)
		}

		kb, err := input.Keyboard(ctx)
		if err != nil {
			s.Fatal("Failed to get keyboard: ", err)
		}
		defer kb.Close()

		crostini.RunCrostiniPostTest(ctx, crostini.PreData{Chrome: cr, TestAPIConn: tconn, Container: cont, Keyboard: kb})

		// Unmount the component.
		vm.UnmountComponent(ctx)
		if err := vm.DeleteImages(); err != nil {
			testing.ContextLogf(ctx, "Error deleting images: %q", err)
		}
	}()
	defer func() { faillog.DumpUITreeAndScreenshot(ctx, tconn, "resize_installation", err) }()
	// Install Crostini.
	resultDiskSize, err := ui.InstallCrostini(ctx, tconn, cr, iOptions)
	if err != nil {
		s.Fatal("Failed to install Crostini: ", err)
	}

	// Get the container.
	cont, err := vm.DefaultContainer(ctx, iOptions.UserName)
	if err != nil {
		s.Fatal("Failed to connect to the container: ", err)
	}

	if err := verifyDiskSize(ctx, tconn, cr, cont, resultDiskSize); err != nil {
		s.Fatal("Failed to verify disk size: ", err)
	}
}

func verifyDiskSize(ctx context.Context, tconn *chrome.TestConn, cr *chrome.Chrome, cont *vm.Container, size uint64) error {
	// Get the keyboard from ctx
	keyboard, err := input.Keyboard(ctx)
	if err != nil {
		return errors.Wrap(err, "failed to get keyboard")
	}
	defer keyboard.Close()

	// Run commands in terminal, which should be visible and functional.
	runInTerminal := func(cmd, outputFile string) error {
		// Open Terminal app.
		terminalApp, err := terminalapp.Find(ctx, tconn)
		if err != nil {
			return errors.Wrap(err, "failed to find Terminal app")
		}
		return uiauto.Combine("running '"+cmd+"'",
			terminalApp.RunCommand(keyboard, fmt.Sprintf("%s > %s 2>&1", cmd, outputFile)),
			terminalApp.WaitForPrompt())(ctx)
	}
	// Check the df results in terminal
	const (
		dfCmd     = "sudo df / -h -l --output=size --type=btrfs"
		dfOutFile = "/tmp/dfout.txt"
	)
	var (
		dfOutStr string
	)
	// Check the disk size with df in Terminal
	if err = runInTerminal(dfCmd, dfOutFile); err != nil {
		return errors.Wrap(err, "failed to run df in terminal")
	}
	if dfOutStr, err = cont.ReadFile(ctx, dfOutFile); err != nil {
		return errors.Wrap(err, "failed to read df output")
	}
	testing.ContextLog(ctx, dfOutStr)

	sizeStr := regexp.MustCompile("[0-9]+").FindAllString(dfOutStr, 1)
	if len(sizeStr) == 0 {
		return errors.Wrap(err, "failed to find disk size info")
	}
	sizeGB, err := strconv.ParseInt(sizeStr[0], 10, 64)
	if err != nil {
		return errors.Wrap(err, "failed to parse disk size")
	}

	actualSize := (int64)(size / settings.SizeGB)

	gap := sizeGB - actualSize
	if gap > 1 || gap < -1 {
		return errors.Wrapf(err, "failed to verify size, got %d, want %d", sizeGB, actualSize)
	}

	// Open the Linux settings.
	st, err := settings.OpenLinuxSettings(ctx, tconn, cr)
	if err != nil {
		return errors.Wrap(err, "failed to open Linux Settings")
	}
	defer st.Close(ctx)

	// Check the disk size on the Settings app.
	sizeOnSettings, err := st.GetDiskSize(ctx)
	if err != nil {
		return errors.Wrap(err, "failed to get the disk size from the Settings app")
	}
	parsedSize, err := settings.ParseDiskSize(sizeOnSettings)
	if err != nil {
		return errors.Wrap(err, "failed to get result disk size in bytes")
	}
	if size != parsedSize {
		return errors.Errorf("failed to verify the disk size on the Settings app, got %v, want %v", parsedSize, size)
	}

	// Check the disk size of the container.
	if err := testing.Poll(ctx, func(ctx context.Context) error {
		disk, err := cont.VM.Concierge.GetVMDiskInfo(ctx, vm.DefaultVMName)
		if err != nil {
			return errors.Wrap(err, "failed to get VM disk info")
		}
		contSize := disk.GetSize()

		// Allow some gap.
		var diff uint64
		if size > contSize {
			diff = size - contSize
		} else {
			diff = contSize - size
		}
		if diff > settings.SizeMB {
			return errors.Errorf("failed to verify disk size, got %d, want approximately %d", contSize, size)
		}
		return nil
	}, &testing.PollOptions{Timeout: 5 * time.Second}); err != nil {
		return errors.Wrap(err, "failed to verify the disk size of the container")
	}

	return nil
}
