func init() {
	testing.AddTest(&testing.Test{
		Func:         VerifySystemInfoAndAppAndMetricsCheckboxState,
		LacrosStatus: testing.LacrosVariantUnneeded,
		Desc:         "Verify the system info and app and metrics checkbox is checked",
		Contacts: []string{
			"wangdanny@google.com",
			"zhangwenyu@google.com",
			"xiangdongkong@google.com",
			"cros-feedback-app@google.com",
		},
		Fixture:      "chromeLoggedInWithOsFeedback",
		Attr:         []string{"group:mainline", "informational"},
		SoftwareDeps: []string{"chrome"},
		Timeout:      2 * time.Minute,
	})
}

// VerifySystemInfoAndAppAndMetricsCheckboxState verifies system info and app and
// metrics checkbox is checked by default.
func VerifySystemInfoAndAppAndMetricsCheckboxState(ctx context.Context, s *testing.State) {
	cr := s.FixtValue().(*chrome.Chrome)

	cleanupCtx := ctx
	ctx, cancel := ctxutil.Shorten(ctx, 5*time.Second)
	defer cancel()

	tconn, err := cr.TestAPIConn(ctx)
	if err != nil {
		s.Fatal("Failed to connect to Test API: ", err)
	}
	defer faillog.DumpUITreeWithScreenshotOnError(cleanupCtx, s.OutDir(), s.HasError, cr,
		"ui_dump")

	ui := uiauto.New(tconn).WithTimeout(20 * time.Second)

	// Launch feedback app and go to share data page.
	feedbackRootNode, err := feedbackapp.LaunchAndGoToShareDataPage(ctx, tconn)
	if err != nil {
		s.Fatal("Failed to launch feedback app and go to share data page: ", err)
	}

	// Verify the system info and app and metrics checkbox is checked by default.
	checkboxAncestor := nodewith.Name("Send system and app info and metrics").Role(
		role.GenericContainer).Ancestor(feedbackRootNode)
	checkbox := nodewith.Role(role.CheckBox).Ancestor(checkboxAncestor)
	if err := ui.WaitUntilExists(checkbox.Attribute("checked", "true"))(ctx); err != nil {
		s.Fatal("Failed to verify the checkbox is checked by default: ", err)
	}
}
