// TabSwitchCUJ2 measures the performance of tab-switching CUJ.
//
// It uses package tabswitchcuj to do testing. The test supports two preconditions:
// wpr.RemoteReplayMode() for running WPR server in a remote server, or wpr.ReplayMode()
// for running WPR server locally in the DUT.
// In order to ensure WPR service won't affect DUT's performance, formal CUJ test should
// use wpr.RemoteReplayMode().
//
// Correspondingly there are two TabSwitchCUJRecorder2 cases that can be used to record
// WPR content for this test in remote or local mode.
func TabSwitchCUJ2(ctx context.Context, s *testing.State) {
	param := s.Param().(cuj.TestParameters)
	tabletMode := param.Category.ScreenMode == cuj.Tablet
	var level tabswitchcuj.Level
	switch param.Category.Tier {
	case cuj.Basic:
		level = tabswitchcuj.Basic
	case cuj.Plus:
		level = tabswitchcuj.Plus
	case cuj.Premium:
		level = tabswitchcuj.Premium
	}

	cr, ok := s.PreValue().(*chrome.Chrome)
	if !ok {
		s.Fatal("Failed to connect to Chrome")
	}

	tabswitchcuj.Run2(ctx, s, cr, level, tabletMode)
}
