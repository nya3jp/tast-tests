// VideoCUJ2 ...
func VideoCUJ2(ctx context.Context, s *testing.State) {
	param := s.Param().(cuj.TestParameters)

	tabletMode := param.Category.ScreenMode == cuj.Tablet

	// Only support basic and plus. Set default to basic.
	tier := cuj.Basic
	if param.Category.Tier == cuj.Plus {
		tier = cuj.Plus
	}

	app := videocuj.YoutubeWeb
	casParam := param.Params.(videoCUJParam)
	if casParam.ApplicationName == "youtube" && casParam.ApplicationType == cuj.APP {
		app = videocuj.YoutubeApp
	} else if casParam.ApplicationName == "netflix" && casParam.ApplicationType == cuj.Web {
		app = videocuj.NetflixWeb
	}

	cr := s.FixtValue().(cuj.FixtureData).Chrome
	a := s.FixtValue().(cuj.FixtureData).ARC
	videocuj.Run(ctx, s, cr, a, app, tabletMode, tier)
}
