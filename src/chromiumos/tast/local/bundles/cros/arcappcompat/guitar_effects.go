// GuitarEffects test uses library for opting into the playstore and installing app.
// Checks GuitarEffects correctly changes the window states in both clamshell and touchview mode.
func GuitarEffects(ctx context.Context, s *testing.State) {
	const (
		appPkgName  = "com.deplike.andrig"
		appActivity = "com.deplike.ui.splash.SplashActivity"
	)
	testSet := s.Param().(testutil.TestParams)
	testutil.RunTestCases(ctx, s, appPkgName, appActivity, testSet)
}

// launchAppForGuitarEffects verifies GuitarEffects is logged in and
// verify GuitarEffects reached main activity page of the app.
func launchAppForGuitarEffects(ctx context.Context, s *testing.State, tconn *chrome.TestConn, a *arc.ARC, d *ui.Device, appPkgName, appActivity string) {
	const (
		letRockText = "Let's Rock"
	)

	// Click on let's allow button to access your photos, media and files.
	letRockButton := d.Object(ui.Text(letRockText))
	if err := letRockButton.WaitForExists(ctx, testutil.DefaultUITimeout); err != nil {
		s.Log(" letRock button doesn't exists: ", err)
	} else if err := letRockButton.Click(ctx); err != nil {
		s.Fatal("Failed to click on letRock button: ", err)
	}

	testutil.HandleDialogBoxes(ctx, s, d, appPkgName)
	// Check for launch verifier.
	launchVerifier := d.Object(ui.PackageName(appPkgName))
	if err := launchVerifier.WaitForExists(ctx, testutil.LongUITimeout); err != nil {
		testutil.DetectAndHandleCloseCrashOrAppNotResponding(ctx, s, d)
		s.Fatal("launchVerifier doesn't exists: ", err)
	}
}
