package org.chromium.arc.testapp.gameperformance;

import java.io.Closeable;
import java.io.IOException;

public class Utils {
    public static void closeQuietly(Closeable closeable) {
        try {
            if (closeable != null) {
                closeable.close();
            }
        } catch (IOException e) {
            // Ignore
        }
    }
}
